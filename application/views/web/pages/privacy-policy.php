    			<br>
    			<p>
    			    The data protection declaration of Pix-Guru.com is based on the terms used by the European legislator for the adoption of the 
        			General Data Protection Regulation (GDPR). Our data protection declaration should be legible and understandable for the general 
        			public, as well as our customers and business partners. To ensure this, we would like to first explain the terminology used.
        		</p>
        		<p>
                    In this data protection declaration, we use, inter alia, the following terms:
                </p>
                <h5>
                    a) Personal Data
                </h5>
                <p>
                    Personal Data means any information relating to an identified or identifiable natural person (“Data Subject”). An identifiable 
                    natural person is one who can be identified, directly or indirectly, in particular by reference to an identifier such as a name, 
                    an identification number, location data, an online identifier or to one or more factors specific to the physical, physiological, 
                    genetic, mental, economic, cultural or social identity of that natural person.
                </p>
                <h5>
                    b) Data Subject
                </h5>
                <p>
                    A Data Subject is any identified or identifiable natural person, whose Personal Data is processed by the Controller responsible for the processing.
                </p>
                <h5>
                    c) Processing
                </h5>
                <p>
                    Processing is any operation or set of operations which is performed on Personal Data or on sets of Personal Data, whether or not 
                    by automated means, such as collection, recording, organisation, structuring, storage, adaptation or alteration, retrieval, 
                    consultation, use, disclosure by transmission, dissemination or otherwise making available, alignment or combination, restriction, erasure or destruction.
                </p>
                <h5>
                    d) Restriction of Processing
                </h5>
                <p>
                    Restriction of Processing is the marking of stored Personal Data with the aim of limiting their processing in the future.
                </p>
                <h5>
                    e) Profiling
                </h5>
                <p>
                    Profiling means any form of automated processing of Personal Data consisting of the use of Personal Data to evaluate certain personal 
                    aspects relating to a natural person, in particular to analyse or predict aspects concerning that natural person's performance at work, 
                    economic situation, health, personal preferences, interests, reliability, behaviour, location or movements.
                </p>
                <h5>
                    f) Pseudonymisation
                </h5>
                <p>
                    Pseudonymisation is the processing of Personal Data in such a manner that the Personal Data can no longer be attributed to a specific
                    Data Subject without the use of additional information, provided that such additional information is kept separately and is subject to 
                    technical and organisational measures to ensure that the Personal Data are not attributed to an identified or identifiable natural person.
                </p>
                <h5>
                    g) Controller or Controller Responsible for the Processing
                </h5>
                <p>
                    The Controller or Controller Responsible for the Processing is the natural or legal person, public authority, agency or other body which, 
                    alone or jointly with others, determines the purposes and means of the processing of Personal Data; where the purposes and means of such 
                    processing are determined by Union or Member State law, the Controller or the specific criteria for its nomination may be provided for 
                    by Union or Member State law.
                </p>
                <h5>
                    h) Processor
                </h5>
                <p>
                    The Processor is a natural or legal person, public authority, agency or other body which processes Personal Data on behalf of the Controller.
                </p>
                <h5>
                    i) Recipient
                </h5>
                <p>
                    The Recipient is a natural or legal person, public authority, agency or another body, to which the Personal Data are disclosed, whether 
                    a third party or not. However, public authorities which may receive Personal Data in the framework of a particular inquiry in accordance 
                    with Union or Member State law shall not be regarded as recipients; the processing of those data by those public authorities shall be in 
                    compliance with the applicable data protection rules according to the purposes of the processing.
                </p>
                <h5>
                    j) Third Party
                </h5>
                <p>
                    A Third Party is a natural or legal person, public authority, agency or body other than the Data Subject, Controller, processor 
                    and persons who, under the direct authority of the Controller or processor, are authorised to process Personal Data.
                </p>
