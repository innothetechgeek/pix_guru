                    cookies doesn’t have to enter access data each time the website is accessed because this function is taken over by the cookie, which is stored on 
                    the user's computer system. Another example is the cookie of a shopping cart in an online shop. The online store remembers the articles that a 
                    customer has placed in the virtual shopping cart via a cookie.
